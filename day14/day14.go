func flowSand(cave *map[int]map[int]string, floorY int, part2 bool) bool {
	newPosX := 500
	newPosY := 0
	settled := false
	abyss := false
	for settled == false {
		if !part2 && newPosY > floorY {
			fmt.Println("Sand starts flowing in the abyss...")
			abyss = true
			break
		}
		if part2 && newPosY >= floorY {
			settled = true
			break
		}

		if (*cave)[newPosX][newPosY+1] == "" {
			newPosY++
		} else if (*cave)[newPosX-1][newPosY+1] == "" {
			newPosX--
			newPosY++
		} else if (*cave)[newPosX+1][newPosY+1] == "" {
			newPosX++
			newPosY++
		} else {
			settled = true
		}
	}

	if !abyss {
		if (*cave)[newPosX] == nil {
			(*cave)[newPosX] = make(map[int]string, 0)
		}
		(*cave)[newPosX][newPosY] = "o"
	}
	return abyss
}

func findMaxYValue(cave *map[int]map[int]string) int {
	max := 0
	for _, v := range *cave {
		for y := range v {
			if y > max {
				max = y
			}
		}
	}
	return max
}

func parseCave(input []string) map[int]map[int]string {
	cave := make(map[int]map[int]string, 0) // xy
	for _, s := range input {
		points := strings.Split(s, " -> ")
		for i := 1; i < len(points); i++ {
			first := strings.Split(points[i-1], ",")
			firstX, _ := strconv.Atoi(first[0])
			firstY, _ := strconv.Atoi(first[1])
			second := strings.Split(points[i], ",")
			secondX, _ := strconv.Atoi(second[0])
			secondY, _ := strconv.Atoi(second[1])

			if firstX != secondX {
				for j := int(math.Min(float64(firstX), float64(secondX))); j <= int(math.Max(float64(firstX), float64(secondX))); j++ {
					if cave[j] == nil {
						cave[j] = make(map[int]string, 0)
					}
					cave[j][firstY] = "#"
				}
			} else if firstY != secondY {
				for j := int(math.Min(float64(firstY), float64(secondY))); j <= int(math.Max(float64(firstY), float64(secondY))); j++ {
					if cave[firstX] == nil {
						cave[firstX] = make(map[int]string, 0)
					}
					cave[firstX][j] = "#"
				}
			}
		}
	}
	return cave
}

