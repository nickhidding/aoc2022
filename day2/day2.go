type score struct {
	round string
	score int
}

func main() {
	rounds := strings.Split(strategies(), "\n")
	scores := make([]score, 0)

	for _, round := range rounds {
		split := strings.Split(round, " ")
		// fmt.Println(split)

		outcomeOfTheRound := outcomeOfTheRoundPart1(split[0], split[1])
		shapeYouSelected := shapeYouSelected(split[1])

		scores = append(scores, score{round: round, score: outcomeOfTheRound + shapeYouSelected})
	}

	sum := 0
	for _, score := range scores {
		fmt.Println(score)
		sum = sum + score.score
	}
	fmt.Println(sum)
	fmt.Println()

	// Part 2
	scores = make([]score, 0)

	for _, round := range rounds {
		split := strings.Split(round, " ")
		// fmt.Println(split)

		myShape := myShape(split[0], split[1])
		// fmt.Println(myShape)
		outcomeOfTheRound := outcomeOfTheRound(split[0], myShape)
		shapeYouSelected := shapeYouSelected(myShape)

		scores = append(scores, score{round: round, score: outcomeOfTheRound + shapeYouSelected})
	}

	sum2 := 0
	for _, score := range scores {
		fmt.Println(score)
		sum2 = sum2 + score.score
	}
	fmt.Println(sum2)
}

func myShape(opponent string, outcome string) string {
	// A for Rock, B for Paper, and C for Scissors
	// X means you need to lose, Y means you need to end the round in a draw, and Z means you need to win.
	me := ""

	switch outcome {
	case "X": // Lose
		switch opponent {
		case "A":
			me = "Z"
		case "B":
			me = "X"
		case "C":
			me = "Y"
		}
		break
	case "Y": // Draw
		switch opponent {
		case "A":
			me = "X"
		case "B":
			me = "Y"
		case "C":
			me = "Z"
		}
		break
	case "Z": // Win
		switch opponent {
		case "A":
			me = "Y"
		case "B":
			me = "Z"
		case "C":
			me = "X"
		}
		break
	}

	return me
}

func outcomeOfTheRoundPart1(opponent string, me string) int {
	if (opponent == "A" && me == "X") || (opponent == "B" && me == "Y") || (opponent == "C" && me == "Z") {
		return 3
	}
	if (opponent == "A" && me == "Y") || (opponent == "B" && me == "Z") || (opponent == "C" && me == "X") {
		return 6
	}
	return 0
}

// 0 if you lost, 3 if the round was a draw, and 6 if you won
func outcomeOfTheRound(opponent string, me string) int {
	// A for Rock, B for Paper, and C for Scissors
	// X for Rock, Y for Paper, and Z for Scissors
	if (opponent == "A" && me == "X") || (opponent == "B" && me == "Y") || (opponent == "C" && me == "Z") {
		return 3
	}
	if (opponent == "A" && me == "Y") || (opponent == "B" && me == "Z") || (opponent == "C" && me == "X") {
		return 6
	}
	return 0
}

// X for Rock, Y for Paper, and Z for Scissors
// 1 for Rock, 2 for Paper, and 3 for Scissors
func shapeYouSelected(shape string) int {
	switch shape {
	case "X":
		return 1
	case "Y":
		return 2
	case "Z":
		return 3
	default:
		return 0
	}
}

